package cmd

import (
	"fmt"
	"os"

	"github.com/spf13/cobra"
)

var cfgFile string

var rootCmd = &cobra.Command{
	Use:   "envoy",
	Short: "envoy-cli — manage environment variable sets across deployment targets",
	Long: `envoy-cli is a lightweight CLI for managing environment variable sets
across multiple deployment targets such as development, staging, and production.`,
}

// Execute runs the root command.
func Execute() {
	if err := rootCmd.Execute(); err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}

func init() {
	rootCmd.PersistentFlags().StringVar(&cfgFile, "config", "", "config file (default: .envoy.yaml)")
}
